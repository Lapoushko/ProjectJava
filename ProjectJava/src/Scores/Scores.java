package Scores;

public class Scores {
    public float totalExercises;
    public float totalPractices;
    public float totalActivities;
    public float totalSeminar;

    public Scores(float ac, float ex, float prac, float sem) {
        this.totalExercises = ex;
        this.totalPractices = prac;
        this.totalActivities = ac;
        this.totalSeminar = sem;
    }
}
